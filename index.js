function add(a , b){
    return a + b;
}
function subtract(b , a ){
    return b - a;
}
function multiply(x , y ){
    return x * y;
}
function divide(x , y ){
     return x / y;
}
function increment(n){
    n++;
    return n;
}
function decrement(n){
    n--;
    return n;
}
function makeInt(n){
    parseInt(n);
    return n;
}
function makeInt(n){
    return parseInt(n, 10);    
}
function preserveDecimal(n){
    return parseFloat(n);    
}